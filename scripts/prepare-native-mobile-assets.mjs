import { cpSync, mkdirSync, rmSync, writeFileSync } from "node:fs";
import path from "node:path";

const root = process.cwd();
const webDir = path.join(root, "dist-mobile");

const appConfig = {
  appId: "com.higino.lyceum.mobile",
  appName: "Lyceum Mobile",
  webDir: "dist-mobile",
  bundledWebRuntime: false,
  server: {
    androidScheme: "https",
  },
  plugins: {
    CapacitorUpdater: {
      autoUpdate: false,
      autoDeleteFailed: true,
      autoDeletePrevious: true,
      appReadyTimeout: 10000,
      statsUrl: "",
    },
    SplashScreen: {
      launchAutoHide: true,
      backgroundColor: "#09090b",
    },
  },
};

const androidPlugins = [
  {
    pkg: "@capacitor/filesystem",
    classpath: "com.capacitorjs.plugins.filesystem.FilesystemPlugin",
  },
  {
    pkg: "@capacitor/preferences",
    classpath: "com.capacitorjs.plugins.preferences.PreferencesPlugin",
  },
  {
    pkg: "@capgo/capacitor-updater",
    classpath: "ee.forgr.capacitor_updater.CapacitorUpdaterPlugin",
  },
];

const iosConfig = {
  ...appConfig,
  packageClassList: ["FilesystemPlugin", "PreferencesPlugin", "CapacitorUpdaterPlugin"],
};

function writeJson(filePath, value) {
  mkdirSync(path.dirname(filePath), { recursive: true });
  writeFileSync(filePath, `${JSON.stringify(value, null, "\t")}\n`);
}

function replaceDir(source, target) {
  rmSync(target, { recursive: true, force: true });
  mkdirSync(path.dirname(target), { recursive: true });
  cpSync(source, target, { recursive: true });
}

function writeAndroidAssets() {
  replaceDir(webDir, path.join(root, "android", "app", "src", "main", "assets", "public"));
  writeJson(path.join(root, "android", "app", "src", "main", "assets", "capacitor.config.json"), appConfig);
  writeJson(path.join(root, "android", "app", "src", "main", "assets", "capacitor.plugins.json"), androidPlugins);

  writeFileSync(
    path.join(root, "android", "app", "src", "main", "res", "xml", "config.xml"),
    "<?xml version='1.0' encoding='utf-8'?>\n" +
      '<widget version="1.0.0" xmlns="http://www.w3.org/ns/widgets" xmlns:cdv="http://cordova.apache.org/ns/1.0">\n' +
      '  <access origin="*" />\n' +
      "</widget>\n",
  );

  const cordovaDir = path.join(root, "android", "capacitor-cordova-android-plugins");
  mkdirSync(path.join(cordovaDir, "src", "main", "java"), { recursive: true });
  mkdirSync(path.join(cordovaDir, "src", "main", "res"), { recursive: true });
  writeFileSync(path.join(cordovaDir, "src", "main", "java", ".gitkeep"), "");
  writeFileSync(path.join(cordovaDir, "src", "main", "res", ".gitkeep"), "");
  writeFileSync(
    path.join(cordovaDir, "cordova.variables.gradle"),
    "// DO NOT EDIT THIS FILE! IT IS GENERATED EACH TIME \"capacitor update\" IS RUN\n" +
      "ext {\n" +
      "  cdvMinSdkVersion = project.hasProperty('minSdkVersion') ? rootProject.ext.minSdkVersion : 24\n" +
      "  // Plugin gradle extensions can append to this to have code run at the end.\n" +
      "  cdvPluginPostBuildExtras = []\n" +
      "  cordovaConfig = [:]\n" +
      "}\n",
  );
  writeFileSync(
    path.join(cordovaDir, "src", "main", "AndroidManifest.xml"),
    "<?xml version='1.0' encoding='utf-8'?>\n" +
      '<manifest xmlns:android="http://schemas.android.com/apk/res/android"\n' +
      'xmlns:amazon="http://schemas.amazon.com/apk/res/android">\n' +
      "<application  >\n\n</application>\n\n</manifest>\n",
  );
  writeFileSync(
    path.join(cordovaDir, "build.gradle"),
    "ext {\n" +
      "    androidxAppCompatVersion = project.hasProperty('androidxAppCompatVersion') ? rootProject.ext.androidxAppCompatVersion : '1.7.1'\n" +
      "    cordovaAndroidVersion = project.hasProperty('cordovaAndroidVersion') ? rootProject.ext.cordovaAndroidVersion : '14.0.1'\n" +
      "}\n\n" +
      "buildscript {\n" +
      "    repositories {\n" +
      "        google()\n" +
      "        mavenCentral()\n" +
      "    }\n" +
      "    dependencies {\n" +
      "        classpath 'com.android.tools.build:gradle:8.13.0'\n" +
      "    }\n" +
      "}\n\n" +
      "apply plugin: 'com.android.library'\n\n" +
      "android {\n" +
      '    namespace = "capacitor.cordova.android.plugins"\n' +
      "    compileSdk = project.hasProperty('compileSdkVersion') ? rootProject.ext.compileSdkVersion : 36\n" +
      "    defaultConfig {\n" +
      "        minSdkVersion project.hasProperty('minSdkVersion') ? rootProject.ext.minSdkVersion : 24\n" +
      "        targetSdkVersion project.hasProperty('targetSdkVersion') ? rootProject.ext.targetSdkVersion : 36\n" +
      "        versionCode 1\n" +
      '        versionName "1.0"\n' +
      "    }\n" +
      "    lintOptions {\n" +
      "        abortOnError = false\n" +
      "    }\n" +
      "    compileOptions {\n" +
      "        sourceCompatibility JavaVersion.VERSION_21\n" +
      "        targetCompatibility JavaVersion.VERSION_21\n" +
      "    }\n" +
      "}\n\n" +
      "repositories {\n" +
      "    google()\n" +
      "    mavenCentral()\n" +
      "    flatDir{\n" +
      "        dirs 'src/main/libs', 'libs'\n" +
      "    }\n" +
      "}\n\n" +
      "dependencies {\n" +
      "    implementation fileTree(dir: 'src/main/libs', include: ['*.jar'])\n" +
      '    implementation "androidx.appcompat:appcompat:$androidxAppCompatVersion"\n' +
      '    implementation "org.apache.cordova:framework:$cordovaAndroidVersion"\n' +
      "    // SUB-PROJECT DEPENDENCIES START\n\n" +
      "    // SUB-PROJECT DEPENDENCIES END\n" +
      "}\n\n" +
      "// PLUGIN GRADLE EXTENSIONS START\n" +
      'apply from: "cordova.variables.gradle"\n' +
      "// PLUGIN GRADLE EXTENSIONS END\n\n" +
      "for (def func : cdvPluginPostBuildExtras) {\n" +
      "    func()\n" +
      "}\n",
  );
}

function writeIosAssets() {
  replaceDir(webDir, path.join(root, "ios", "App", "App", "public"));
  writeJson(path.join(root, "ios", "App", "App", "capacitor.config.json"), iosConfig);
}

writeAndroidAssets();
writeIosAssets();

console.log("Prepared native mobile assets for Android and iOS.");
